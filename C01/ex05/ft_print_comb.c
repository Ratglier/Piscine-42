#include <unistd.h>

void	ft_print_comb(void)
{
	int		x;
	int		y;
	int		z;

	x = 48;
	while ((x - 48) < 8)
	{
		y = x + 1;
		while ((y - 48) < 9)
		{
			z = y + 1;
			while ((z - 48) < 10)
			{
				write(1, &x, 1);
				write(1, &y, 1);
				write(1, &z, 1);
				if ((x - 48) != 7)
					write(1, ", ", 2);
				z++;
			}
			y++;
		}
		x++;
	}
}
