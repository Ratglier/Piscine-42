int		ft_is_prime(int nb)
{
	int x;

	x = 2;
	if (nb < 2)
		return (0);
	while (x <= nb / x)
	{
		if (nb % 2 == 0 || nb < 2 || nb % x == 0)
			return (0);
		x++;
	}
	return (1);
}
