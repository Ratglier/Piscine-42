int		ft_strcmp(char *s1, char *s2)
{
	int x;

	x = 0;
	while (s1[x] && s2[x])
	{
		if ((s1[x]) > (s2[x]))
			return (s1[x] - s2[x]);
		else if ((s1[x]) < (s2[x]))
			return (s1[x] - s2[x]);
		x++;
	}
	if (s1[x] && !s2[x])
		return (1);
	else if (!s1[x] && s2[x])
		return (-1);
	return (0);
}
